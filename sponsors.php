<h1>Sponsorships</h1>

<p>Make a generous contribution today. Become a partner in making a difference for those impacted by cancer. Through your support we can continue to touch and lift the spirit of those traveling their difficult cancer journey. It also allows us to reach globally to those in need of hope. It is through your generosity that will empower the patient, the survivor and their families through your acts of kindness.</p>

<p>Although The Cup With Love Project is in its infancy; supporters from across the nation have come to our side to help us out reach to those impacted by cancer.</p>

<p>Through their expertise and service it has allowed this organization to conduct fundraisers, presentations, and provide material and supplies needed in fulfilling the requests for Cups With Love.</p>

<p>Our Cups With Love have traveled throughout the United States and have been sent to New Zealand, Philippines, South America, Australia and United Kingdom.</p>

<p>If you are interested in participating as a sponsor please <a href="/contact.php" title="Send Cup With Love an email">contact us</a>.</p>

