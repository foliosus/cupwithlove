<h1>About us</h1>

<h2>Genesis</h2>

<div class="genesis_story">
  <p>The Cup With Love Project was founded in a living room, over a cup of tea and amongst good friends.  Inspired by simple, but thoughtful acts of kindness bestowed upon her during her cancer treatment, Cup With Love founder, Molly Ximenez was inspired to share that same compassion with newly diagnosed cancer patients.  The concept of gifting a tea cup to those on a cancer journey, or those whose lives have been impacted by cancer, grew from these humble beginnings and today has profoundly touched the lives of individuals world-wide.</p>
  <p>Molly's vision lead to the formal creation of &#8220;the Cup With Love Project&#8221; in 2005, as a recognized not-for-profit organization.  Since then, the Cup With Love Project has collected cups, wrapped and provided those gifts of love to thousands of cancer survivors undergoing treatment.  Thanks both to the continued efforts of Molly and a band of loving volunteers, the same acts of compassion and kindness continue.</p>
  <p>Sacramento, California (USA)-based, the Cup With Love Project serves to empower those traveling through their cancer journey with love and hope, by letting them know they are not alone and that they have the inner strength to fight the fight through the Cup With Love Project community.</p>
</div>

