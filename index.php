<div id="mission">
  <h3>Our mission</h3>
  <p>The Cup With Love Project is a non-profit organization that represents world-wide acts of kindness to individuals impacted by cancer. The Cup With Love Project's commitment is to provide emotional support to cancer survivors, patients undergoing cancer treatment and those impacted by cancer.</p>

  <p>A gifted cup symbolizes love, hope, prayers for inner strength, and serves to empower them as they travel through their cancer journey. It is a true testament that lets them know that they are not alone.</p>

  <p>The Cup With Love Project also provides advocacy, mentoring, education on cancer prevention, and raises awareness on cancer-related health issues. Through these combined efforts it benefits and strengthens all communities.</p>
</div>

